class MaskedWeightedBCELoss(nn.Module):
    def __init__(self, pos_weight=23.0):
        super().__init__()
        self.register_buffer("pos_weight", torch.tensor([pos_weight], dtype=torch.float32))

    def forward(self, logits, targets, valid):
        loss = F.binary_cross_entropy_with_logits(logits, targets, pos_weight=self.pos_weight, reduction="none")
        return (loss * valid).sum() / (valid.sum() + 1e-8)


def counts_to_metrics(tp, fp, fn, tn):
    precision = tp / (tp + fp + 1e-8)
    recall = tp / (tp + fn + 1e-8)
    f1 = 2.0 * precision * recall / (precision + recall + 1e-8)
    iou = tp / (tp + fp + fn + 1e-8)
    dice = 2.0 * tp / (2.0 * tp + fp + fn + 1e-8)
    accuracy = (tp + tn) / (tp + tn + fp + fn + 1e-8)
    return {"precision": precision, "recall": recall, "f1": f1, "iou": iou, "dice": dice, "accuracy": accuracy}


def compute_counts_from_logits(logits, targets, valid, threshold):
    probs = torch.sigmoid(logits)
    preds = (probs >= threshold).float()
    preds = preds[valid == 1]
    targets = targets[valid == 1]
    tp = int(((preds == 1) & (targets == 1)).sum().item())
    fp = int(((preds == 1) & (targets == 0)).sum().item())
    fn = int(((preds == 0) & (targets == 1)).sum().item())
    tn = int(((preds == 0) & (targets == 0)).sum().item())
    return tp, fp, fn, tn


def evaluate_model(model, loader, criterion, threshold=0.5, desc=None):
    model.eval()
    total_loss, total_tp, total_fp, total_fn, total_tn = 0.0, 0, 0, 0, 0
    desc = desc or f"Evaluating th={threshold:.3f}"
    with torch.no_grad():
        for images, targets, valid in tqdm(loader, desc=desc, leave=False):
            images = images.to(device, non_blocking=True)
            targets = targets.to(device, non_blocking=True)
            valid = valid.to(device, non_blocking=True)
            with torch.autocast(device_type=device.type, dtype=torch.float16, enabled=(USE_AMP and device.type == "cuda")):
                logits = model(images)
                loss = criterion(logits, targets, valid)
            total_loss += float(loss.item())
            tp, fp, fn, tn = compute_counts_from_logits(logits, targets, valid, threshold=threshold)
            total_tp += tp; total_fp += fp; total_fn += fn; total_tn += tn
    metrics = counts_to_metrics(total_tp, total_fp, total_fn, total_tn)
    metrics.update({"loss": total_loss / max(len(loader), 1), "tp": total_tp, "fp": total_fp, "fn": total_fn, "tn": total_tn, "threshold": threshold})
    return metrics


def validation_threshold_sweep(model, loader, criterion):
    model.eval()
    bin_edges = np.linspace(0.0, 1.0, 1001)
    pos_hist = np.zeros(1000, dtype=np.float64)
    neg_hist = np.zeros(1000, dtype=np.float64)
    total_loss = 0.0

    with torch.no_grad():
        for images, targets, valid in tqdm(loader, desc="Validation probability histograms"):
            images = images.to(device, non_blocking=True)
            targets = targets.to(device, non_blocking=True)
            valid = valid.to(device, non_blocking=True)
            with torch.autocast(device_type=device.type, dtype=torch.float16, enabled=(USE_AMP and device.type == "cuda")):
                logits = model(images)
                loss = criterion(logits, targets, valid)
            total_loss += float(loss.item())
            probs = torch.sigmoid(logits).detach().float().cpu().numpy()
            targets_np = targets.detach().cpu().numpy()
            valid_np = valid.detach().cpu().numpy()
            valid_pixels = valid_np == 1
            probs_valid = probs[valid_pixels]
            targets_valid = targets_np[valid_pixels]
            if probs_valid.size == 0:
                continue
            pos_probs = probs_valid[targets_valid == 1]
            neg_probs = probs_valid[targets_valid == 0]
            if pos_probs.size > 0:
                pos_hist += np.histogram(pos_probs, bins=bin_edges)[0]
            if neg_probs.size > 0:
                neg_hist += np.histogram(neg_probs, bins=bin_edges)[0]

    pos_cum_high = np.cumsum(pos_hist[::-1])[::-1]
    neg_cum_high = np.cumsum(neg_hist[::-1])[::-1]
    total_pos = pos_hist.sum()
    total_neg = neg_hist.sum()
    thresholds = np.round(np.arange(THRESHOLD_MIN, THRESHOLD_MAX + 0.5 * THRESHOLD_STEP, THRESHOLD_STEP), 3)
    rows = []
    for th in thresholds:
        bin_idx = int(np.floor(th * 1000.0))
        bin_idx = max(0, min(bin_idx, 999))
        tp = pos_cum_high[bin_idx]
        fp = neg_cum_high[bin_idx]
        fn = total_pos - tp
        tn = total_neg - fp
        metrics = counts_to_metrics(tp, fp, fn, tn)
        rows.append({
            "threshold": float(th),
            "loss": total_loss / max(len(loader), 1),
            "tp": int(tp), "fp": int(fp), "fn": int(fn), "tn": int(tn),
            "precision": metrics["precision"], "recall": metrics["recall"],
            "f1": metrics["f1"], "iou": metrics["iou"], "dice": metrics["dice"], "accuracy": metrics["accuracy"],
        })
    sweep_df = pd.DataFrame(rows)
    sweep_df.to_csv(VAL_THRESHOLD_CSV, index=False)
    best = sweep_df.loc[sweep_df["iou"].idxmax()].to_dict()
    print(f"Validation threshold sweep saved to: {VAL_THRESHOLD_CSV}")
    print(f"Best validation threshold: {best['threshold']:.3f} | IoU={best['iou']:.4f} | Dice={best['dice']:.4f} | Precision={best['precision']:.4f} | Recall={best['recall']:.4f}")
    return sweep_df, float(best["threshold"])

