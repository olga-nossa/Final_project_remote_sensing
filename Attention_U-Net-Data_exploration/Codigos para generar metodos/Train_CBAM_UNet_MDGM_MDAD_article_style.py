def read_rgb(path: Path) -> np.ndarray:
    with Image.open(path) as img:
        return np.asarray(img.convert("RGB"))


def read_mask(path: Path) -> np.ndarray:
    with Image.open(path) as img:
        return np.asarray(img.convert("L")).astype(np.uint8)


def find_mask_for_image(mask_dir: Path, image_path: Path) -> Path:
    stem = image_path.stem
    direct = mask_dir / f"{stem}_mask_class.png"
    if direct.exists():
        return direct
    candidates = sorted(mask_dir.glob(f"{stem}*mask*.png"))
    if candidates:
        return candidates[0]
    candidates = sorted(mask_dir.glob(f"*{stem}*.png"))
    if candidates:
        return candidates[0]
    raise FileNotFoundError(f"No mask found for image: {image_path}")


def load_year_records(year: str) -> pd.DataFrame:
    year_dir = ROOT_DIR / year
    report_csv = year_dir / "reports" / "selected_top100.csv"
    rows = []

    if report_csv.exists():
        df = pd.read_csv(report_csv).copy()
        for _, row in df.iterrows():
            image_path = ROOT_DIR / str(row["image_path"])
            mask_path = ROOT_DIR / str(row["mask_png_path"])

            if not image_path.exists():
                alt = year_dir / "selected_top100" / "images" / image_path.name
                image_path = alt if alt.exists() else image_path
            if not mask_path.exists():
                alt = year_dir / "selected_top100" / "masks" / mask_path.name
                mask_path = alt if alt.exists() else mask_path

            if image_path.exists() and mask_path.exists():
                rows.append({
                    "year": year,
                    "idx": int(row["idx"]) if "idx" in row and not pd.isna(row["idx"]) else len(rows),
                    "day_key": str(row["day_key"]) if "day_key" in row else image_path.stem,
                    "image_path": str(image_path),
                    "mask_path": str(mask_path),
                    "storm_pct_valid": float(row["storm_pct_valid"]) if "storm_pct_valid" in row else np.nan,
                })
    else:
        image_dir = year_dir / "selected_top100" / "images"
        mask_dir = year_dir / "selected_top100" / "masks"
        image_paths = []
        for ext in ["*.png", "*.jpg", "*.jpeg", "*.tif", "*.tiff"]:
            image_paths.extend(sorted(image_dir.glob(ext)))
        for i, image_path in enumerate(sorted(image_paths)):
            mask_path = find_mask_for_image(mask_dir, image_path)
            rows.append({
                "year": year,
                "idx": i,
                "day_key": image_path.stem,
                "image_path": str(image_path),
                "mask_path": str(mask_path),
                "storm_pct_valid": np.nan,
            })

    out = pd.DataFrame(rows)
    if out.empty:
        raise RuntimeError(f"No image-mask pairs found for {year}.")

    # Keep the same selected top-100 set, but sort by MDAD time index before splitting.
    out = out.sort_values("idx").head(100).reset_index(drop=True)
    print(f"{year}: loaded {len(out)} selected image-mask pairs.")
    return out


def split_records_per_year(records_by_year: Dict[str, pd.DataFrame]) -> pd.DataFrame:
    split_rows = []
    for year, df in records_by_year.items():
        df = df.sort_values("idx").reset_index(drop=True)
        n = len(df)
        n_train = int(round(n * TRAIN_FRAC))
        n_val = int(round(n * VAL_FRAC))
        n_test = n - n_train - n_val

        df = df.copy()
        df["split"] = ["train"] * n_train + ["val"] * n_val + ["test"] * n_test
        split_rows.append(df)
        print(f"{year}: train={n_train}, val={n_val}, test={n_test}, total={n}")

    split_df = pd.concat(split_rows, ignore_index=True)
    split_df.to_csv(SPLIT_CSV, index=False)
    print(f"Image split manifest saved to: {SPLIT_CSV}")
    return split_df

