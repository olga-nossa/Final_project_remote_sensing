class MDGMPatchDataset(Dataset):
    def __init__(self, patch_inventory: pd.DataFrame, mean: np.ndarray, std: np.ndarray, augment_4x: bool = False):
        self.inv = patch_inventory.reset_index(drop=True)
        self.mean = mean.astype(np.float32)
        self.std = std.astype(np.float32)
        self.augment_4x = augment_4x
        self.multiplier = 4 if augment_4x else 1

    def __len__(self):
        return len(self.inv) * self.multiplier

    def __getitem__(self, index):
        base_index = index % len(self.inv)
        aug_id = index // len(self.inv) if self.augment_4x else 0
        row = self.inv.iloc[base_index]

        rgb, mask = load_preprocessed_pair_cached(row["image_path"], row["mask_path"])
        x0, y0, x1, y1 = int(row["x0"]), int(row["y0"]), int(row["x1"]), int(row["y1"])

        img_patch = rgb[y0:y1, x0:x1].astype(np.float32) / 255.0
        mask_patch = mask[y0:y1, x0:x1].astype(np.uint8)

        target = (mask_patch == 1).astype(np.float32)
        valid = (mask_patch != 2).astype(np.float32)

        if aug_id == 1:
            img_patch = np.flip(img_patch, axis=1).copy()
            target = np.flip(target, axis=1).copy()
            valid = np.flip(valid, axis=1).copy()
        elif aug_id == 2:
            img_patch = np.flip(img_patch, axis=0).copy()
            target = np.flip(target, axis=0).copy()
            valid = np.flip(valid, axis=0).copy()
        elif aug_id == 3:
            img_patch = np.flip(np.flip(img_patch, axis=0), axis=1).copy()
            target = np.flip(np.flip(target, axis=0), axis=1).copy()
            valid = np.flip(np.flip(valid, axis=0), axis=1).copy()

        img_patch = (img_patch - self.mean) / (self.std + 1e-8)
        img_patch = np.transpose(img_patch, (2, 0, 1))
        target = target[None, :, :]
        valid = valid[None, :, :]

        return (
            torch.tensor(img_patch, dtype=torch.float32),
            torch.tensor(target, dtype=torch.float32),
            torch.tensor(valid, dtype=torch.float32),
        )


def compute_train_mean_std(train_image_df: pd.DataFrame) -> Tuple[np.ndarray, np.ndarray]:
    total_sum = np.zeros(3, dtype=np.float64)
    total_sq = np.zeros(3, dtype=np.float64)
    total_n = 0

    for _, row in tqdm(train_image_df.iterrows(), total=len(train_image_df), desc="Computing train mean/std"):
        rgb, mask = load_preprocessed_pair_cached(str(row["image_path"]), str(row["mask_path"]))
        valid = mask != 2
        pixels = rgb[valid].astype(np.float32) / 255.0
        if pixels.size == 0:
            continue
        total_sum += pixels.sum(axis=0)
        total_sq += (pixels ** 2).sum(axis=0)
        total_n += pixels.shape[0]

    if total_n == 0:
        raise RuntimeError("No valid pixels found for normalization.")

    mean = total_sum / total_n
    var = total_sq / total_n - mean ** 2
    std = np.sqrt(np.maximum(var, 1e-8))
    return mean.astype(np.float32), std.astype(np.float32)

