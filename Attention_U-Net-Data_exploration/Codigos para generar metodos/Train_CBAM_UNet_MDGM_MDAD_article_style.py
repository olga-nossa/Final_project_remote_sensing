def predict_single_patch(model, row, mean, std):
    rgb, mask = load_preprocessed_pair_cached(row["image_path"], row["mask_path"])
    x0, y0, x1, y1 = int(row["x0"]), int(row["y0"]), int(row["x1"]), int(row["y1"])
    img_patch = rgb[y0:y1, x0:x1].astype(np.float32) / 255.0
    mask_patch = mask[y0:y1, x0:x1].astype(np.uint8)
    target = (mask_patch == 1).astype(np.uint8)
    valid = (mask_patch != 2).astype(np.uint8)
    img_norm = (img_patch - mean) / (std + 1e-8)
    img_norm = np.transpose(img_norm, (2, 0, 1))
    x = torch.tensor(img_norm[None, :, :, :], dtype=torch.float32).to(device)
    model.eval()
    with torch.no_grad():
        with torch.autocast(device_type=device.type, dtype=torch.float16, enabled=(USE_AMP and device.type == "cuda")):
            logits = model(x)
            probs = torch.sigmoid(logits)
    prob_map = probs.squeeze().detach().float().cpu().numpy()
    return img_patch, target, valid, prob_map


def plot_confusion_matrix(metrics: dict):
    cm = np.array([[metrics["tn"], metrics["fp"]], [metrics["fn"], metrics["tp"]]], dtype=np.int64)
    fig, ax = plt.subplots(figsize=(6, 5))
    im = ax.imshow(cm)
    ax.set_title(f"Test Confusion Matrix\nThreshold={metrics['threshold']:.3f} | IoU={metrics['iou']:.4f} | Dice={metrics['dice']:.4f}")
    ax.set_xticks([0, 1]); ax.set_yticks([0, 1])
    ax.set_xticklabels(["Pred no storm", "Pred storm"])
    ax.set_yticklabels(["True no storm", "True storm"])
    ax.set_xlabel("Predicted"); ax.set_ylabel("Ground truth")
    for i in range(2):
        for j in range(2):
            ax.text(j, i, f"{cm[i, j]:,}", ha="center", va="center")
    fig.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
    fig.tight_layout()
    fig.savefig(CONFUSION_MATRIX_FIG, dpi=300, bbox_inches="tight")
    plt.close(fig)
    print(f"Confusion matrix figure saved to: {CONFUSION_MATRIX_FIG}")


def plot_test_examples(model, test_inv, mean, std, threshold):
    positive = test_inv[test_inv["positive_patch"]].copy()
    if len(positive) >= N_TEST_VISUALIZATIONS:
        selected = positive.sort_values("storm_ratio_valid", ascending=False).head(N_TEST_VISUALIZATIONS).reset_index(drop=True)
    else:
        selected = test_inv.sample(n=min(N_TEST_VISUALIZATIONS, len(test_inv)), random_state=SEED).reset_index(drop=True)

    n = len(selected)
    fig, axes = plt.subplots(nrows=n, ncols=3, figsize=(13, 4.2 * n))
    if n == 1:
        axes = np.expand_dims(axes, axis=0)

    for i, (_, row) in enumerate(selected.iterrows()):
        img_patch, target, valid, prob_map = predict_single_patch(model, row, mean, std)
        pred = (prob_map >= threshold).astype(np.uint8)
        pred[valid == 0] = 0
        gt_display = target.copy(); gt_display[valid == 0] = 2
        pred_display = pred.copy(); pred_display[valid == 0] = 2

        axes[i, 0].imshow(img_patch)
        axes[i, 0].set_title(f"Original patch\n{row['year']} | {row['day_key']}\nstorm={row['storm_ratio_valid']:.2%}")
        axes[i, 0].axis("off")
        axes[i, 1].imshow(gt_display, vmin=0, vmax=2)
        axes[i, 1].set_title("Ground-truth mask")
        axes[i, 1].axis("off")
        axes[i, 2].imshow(pred_display, vmin=0, vmax=2)
        axes[i, 2].set_title(f"Prediction\nthreshold={threshold:.3f}")
        axes[i, 2].axis("off")

    fig.tight_layout()
    fig.savefig(TEST_VIS_FIG, dpi=300, bbox_inches="tight")
    plt.close(fig)
    print(f"Test visualization saved to: {TEST_VIS_FIG}")

