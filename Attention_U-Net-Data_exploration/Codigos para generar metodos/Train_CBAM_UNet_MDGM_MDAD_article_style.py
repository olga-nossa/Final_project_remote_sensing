def detect_black(rgb: np.ndarray) -> np.ndarray:
    return np.all(rgb <= BLACK_THRESHOLD, axis=2)


def trim_black_borders(rgb: np.ndarray, mask: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    black = detect_black(rgb)
    h, w = black.shape

    top = 0
    while top < h - 1 and black[top, :].mean() >= BORDER_BLACK_FRAC:
        top += 1

    bottom = h - 1
    while bottom > top and black[bottom, :].mean() >= BORDER_BLACK_FRAC:
        bottom -= 1

    left = 0
    while left < w - 1 and black[:, left].mean() >= BORDER_BLACK_FRAC:
        left += 1

    right = w - 1
    while right > left and black[:, right].mean() >= BORDER_BLACK_FRAC:
        right -= 1

    return rgb[top:bottom + 1, left:right + 1], mask[top:bottom + 1, left:right + 1]


def wrap_pad_east_west(rgb: np.ndarray, mask: np.ndarray, pad: int = WRAP_PAD_X) -> Tuple[np.ndarray, np.ndarray]:
    if pad <= 0 or mask.shape[1] <= 2 * pad:
        return rgb, mask
    rgb_padded = np.concatenate([rgb[:, -pad:, :], rgb, rgb[:, :pad, :]], axis=1)
    mask_padded = np.concatenate([mask[:, -pad:], mask, mask[:, :pad]], axis=1)
    return rgb_padded, mask_padded


def preprocess_pair(rgb: np.ndarray, mask: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    if rgb.shape[:2] != mask.shape:
        raise ValueError(f"RGB and mask shapes do not match: rgb={rgb.shape}, mask={mask.shape}")

    mask = mask.copy()
    mask[detect_black(rgb)] = 2
    rgb, mask = trim_black_borders(rgb, mask)
    mask[detect_black(rgb)] = 2
    rgb, mask = wrap_pad_east_west(rgb, mask, WRAP_PAD_X)
    return rgb, mask


@lru_cache(maxsize=24)
def load_preprocessed_pair_cached(image_path_str: str, mask_path_str: str) -> Tuple[np.ndarray, np.ndarray]:
    rgb = read_rgb(Path(image_path_str))
    mask = read_mask(Path(mask_path_str))
    return preprocess_pair(rgb, mask)


def build_patch_inventory(split_df: pd.DataFrame) -> pd.DataFrame:
    patch_rows = []

    for _, row in tqdm(split_df.iterrows(), total=len(split_df), desc="Building patch inventory"):
        rgb, mask = load_preprocessed_pair_cached(str(row["image_path"]), str(row["mask_path"]))
        h, w = mask.shape
        if h < PATCH_H or w < PATCH_W:
            continue

        for y0 in range(0, h - PATCH_H + 1, STRIDE):
            for x0 in range(0, w - PATCH_W + 1, STRIDE):
                y1, x1 = y0 + PATCH_H, x0 + PATCH_W
                mask_patch = mask[y0:y1, x0:x1]
                valid = mask_patch != 2
                valid_ratio = float(valid.mean())

                # Article criterion: discard if black/no-data pixels are more than informative pixels.
                if valid_ratio <= MIN_VALID_RATIO:
                    continue

                storm_ratio_valid = float(np.mean(mask_patch[valid] == 1)) if valid.any() else 0.0
                storm_pixels = int((mask_patch == 1).sum())

                patch_rows.append({
                    "year": row["year"],
                    "idx": row["idx"],
                    "day_key": row["day_key"],
                    "split": row["split"],
                    "image_path": str(row["image_path"]),
                    "mask_path": str(row["mask_path"]),
                    "x0": x0,
                    "y0": y0,
                    "x1": x1,
                    "y1": y1,
                    "valid_ratio": valid_ratio,
                    "storm_ratio_valid": storm_ratio_valid,
                    "storm_pixels": storm_pixels,
                    "positive_patch": storm_pixels > 0,
                })

    inv = pd.DataFrame(patch_rows)
    if inv.empty:
        raise RuntimeError("No patches were generated. Check paths and patch parameters.")

    inv.to_csv(PATCH_CSV, index=False)
    print(f"Patch inventory saved to: {PATCH_CSV}")
    print("Patch count by split:")
    print(inv["split"].value_counts())
    print("Positive patch fraction by split:")
    print(inv.groupby("split")["positive_patch"].mean())
    return inv

