def train_model(model, train_loader, val_loader, criterion, train_mean, train_std):
    optimizer = torch.optim.Adam(model.parameters(), lr=LEARNING_RATE)
    scaler = torch.cuda.amp.GradScaler(enabled=(USE_AMP and device.type == "cuda"))
    best_val_iou, best_epoch = 0.0, 0
    history = []

    for epoch in range(NUM_EPOCHS):
        model.train()
        train_loss = 0.0
        for images, targets, valid in tqdm(train_loader, desc=f"Epoch {epoch + 1}/{NUM_EPOCHS}"):
            images = images.to(device, non_blocking=True)
            targets = targets.to(device, non_blocking=True)
            valid = valid.to(device, non_blocking=True)
            optimizer.zero_grad(set_to_none=True)
            with torch.autocast(device_type=device.type, dtype=torch.float16, enabled=(USE_AMP and device.type == "cuda")):
                logits = model(images)
                loss = criterion(logits, targets, valid)
            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()
            train_loss += float(loss.item())
        train_loss /= max(len(train_loader), 1)

        val_metrics = evaluate_model(model, val_loader, criterion, threshold=0.5, desc="Validation @0.5")
        row = {
            "epoch": epoch + 1, "train_loss": train_loss, "val_loss": val_metrics["loss"],
            "val_precision": val_metrics["precision"], "val_recall": val_metrics["recall"],
            "val_f1": val_metrics["f1"], "val_iou": val_metrics["iou"], "val_dice": val_metrics["dice"],
            "learning_rate": optimizer.param_groups[0]["lr"],
        }
        history.append(row)
        pd.DataFrame(history).to_csv(HISTORY_CSV, index=False)
        print(f"Epoch [{epoch + 1:03d}/{NUM_EPOCHS}] | T.Loss={train_loss:.4f} | V.Loss={val_metrics['loss']:.4f} | IoU={val_metrics['iou']:.4f} | Dice={val_metrics['dice']:.4f} | Prec={val_metrics['precision']:.4f} | Rec={val_metrics['recall']:.4f}")

        if val_metrics["iou"] > best_val_iou:
            best_val_iou = val_metrics["iou"]
            best_epoch = epoch + 1
            torch.save({
                "model_state_dict": model.state_dict(),
                "optimizer_state_dict": optimizer.state_dict(),
                "epoch": best_epoch,
                "best_val_iou_at_05": best_val_iou,
                "base_channels": BASE_CHANNELS,
                "patch_h": PATCH_H,
                "patch_w": PATCH_W,
                "stride": STRIDE,
                "pos_weight": POS_WEIGHT,
                "learning_rate": LEARNING_RATE,
                "mean": train_mean.tolist(),
                "std": train_std.tolist(),
                "years": YEARS,
                "split": {"train": TRAIN_FRAC, "val": VAL_FRAC, "test": TEST_FRAC},
            }, BEST_MODEL_PATH)
            print(f"-> New best model saved: epoch={best_epoch}, val IoU@0.5={best_val_iou:.4f}")

    print(f"Training completed. Best epoch: {best_epoch}, best val IoU@0.5: {best_val_iou:.4f}")
    print(f"Best model saved to: {BEST_MODEL_PATH}")
    return pd.DataFrame(history)

